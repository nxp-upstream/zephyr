#define HELP_NONE "[none]"

SHELL_STATIC_SUBCMD_SET_CREATE(pbap_pce_cmds,
		SHELL_SUBCMD_SET_END
);

static int cmd_pbap_rfcomm(const struct shell *sh, size_t argc, char **argv)
{
	if (argc == 1) {
		shell_help(sh);
		/* sh returns 1 when help is printed */
		return 1;
	}

	shell_error(sh, "%s unknown parameter: %s", argv[0], argv[1]);

	return -ENOEXEC;
}

SHELL_CMD_ARG_REGISTER(pbap_rfcomm_test, &pbap_pce_cmds, "Bluetooth test pbap rfcomm test sh commands",
		       cmd_pbap_rfcomm, 1, 1);